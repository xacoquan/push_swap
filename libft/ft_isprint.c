#include "includes/libft.h"

int		ft_isprint(int c)
{
	if (c == '\0')
		return (0);
	if (ft_isalnum(c) || ft_strchr(" !\"#$%&\'()*+,-./:;<=>?@[\\]^_`{|}~", c))
		return (1);
	return (0);
}
